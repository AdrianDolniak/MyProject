using System;

namespace ProjectCSharp.program
{
    public static class Calculator
    {
        public static float Multiply(float arg1, float arg2)
        {
            return (arg1 * arg2);
        }

        public static float Divide(float arg1, float arg2)
        {
            return (arg1 / arg2);
        }

        public static float Adding(float arg1, float arg2)
        {
            return (arg1 + arg2);
        }

        public static float Subtract(float arg1, float arg2)
        {
            return (arg1 - arg2);
        }

        public static float Modulo(float arg1, float arg2)
        {
            return (arg1 % arg2);
        }

    }

}